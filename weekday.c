// Write a C program to input week number and print week day.
#include<stdio.h>
void main()
{
int week;
printf("enter week number to print week day");
scanf("%d",&week);
 if(week==1)
{
	printf("monday");
}	
 if(week==2)
{
	printf("Tuesday");
}	
 if(week==3)
{
	printf("wednesday");
}	
 if(week==4)
{
	printf("thursday");
}	
 if(week==5)
{
	printf("friday");
}	
 if(week==6)
{
	printf("saturday");
}
 if(week==7)
{
	printf("sunday");
}		

}
